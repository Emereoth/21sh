#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = 0;
	while (*s)
	{
		i++;
		s++;
	}
	while (*s != (char)c && i > 0)
	{
		s--;
		i--;
	}
	if (i == 0 && *s != (char)c)
		return (NULL);
	return ((char*)s);
}

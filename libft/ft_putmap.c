#include "libft.h"

void	ft_putmap(char **c)
{
	int y;

	if (!c)
		return ;
	y = 0;
	while (c[y])
	{
		ft_putstr(c[y]);
		ft_putchar('\n');
		ft_putendl("--------------------");
		y++;
	}
}
